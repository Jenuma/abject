// ---------------------------------------------------------------------------------------------------------|
// Route Directory                                                                                          |
// ---------------------------------------------------------------------------------------------------------|
module.exports = function(passport) {
    var router = require("express").Router();
    
    // -----------------------------------------------------------------------------------------------------|
    // Session                                                                                              |
    // -----------------------------------------------------------------------------------------------------|
    router.use("/session", require("./controllers/session-controller")(passport));
    
    // -----------------------------------------------------------------------------------------------------|
    // API                                                                                                  |
    // -----------------------------------------------------------------------------------------------------|
    router.use("/api/contacts", require("./controllers/contact-controller")());
    
    return router;
};
